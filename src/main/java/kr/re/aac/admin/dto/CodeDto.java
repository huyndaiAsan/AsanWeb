package kr.re.aac.admin.dto;

public class CodeDto {
	
	private String cdType;
	
	private String cd;
	
	private String cdNm;
	
	private String cdSno;
	
	private String cdUseFl;
	
	private String cdDesc;
	
	private String cdRule;

	public String getCdType() {
		return cdType;
	}

	public void setCdType(String cdType) {
		this.cdType = cdType;
	}

	public String getCd() {
		return cd;
	}

	public void setCd(String cd) {
		this.cd = cd;
	}

	public String getCdNm() {
		return cdNm;
	}

	public void setCdNm(String cdNm) {
		this.cdNm = cdNm;
	}

	public String getCdSno() {
		return cdSno;
	}

	public void setCdSno(String cdSno) {
		this.cdSno = cdSno;
	}

	public String getCdUseFl() {
		return cdUseFl;
	}

	public void setCdUseFl(String cdUseFl) {
		this.cdUseFl = cdUseFl;
	}

	public String getCdDesc() {
		return cdDesc;
	}

	public void setCdDesc(String cdDesc) {
		this.cdDesc = cdDesc;
	}

	public String getCdRule() {
		return cdRule;
	}

	public void setCdRule(String cdRule) {
		this.cdRule = cdRule;
	}
	
	
}
